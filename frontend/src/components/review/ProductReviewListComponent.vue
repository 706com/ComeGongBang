<script>
import { mapActions, mapState } from 'pinia';
import { useReviewStore } from '@/stores/useReviewStore';
import { useRoute } from 'vue-router';

export default {
    name: 'ProductReviewListComponent',

    data() {
        return {
            productIdx: null,
        };
    },
    computed: {
        ...mapState(useReviewStore, ['reviewList', 'page', 'avgScore']),
        filteredPages() {
            const pages = [];
            const { number, totalPages } = this.page;
            if (totalPages <= 5) {
                for (let n = 1; n <= totalPages; n++) {
                    pages.push(n);
                }
            }
            else if (number <= 5) {
                for (let n = 1; n <= 7; n++) { // 1 2 3 4 5 6 7
                    pages.push(n);
                }
                pages.push('...');
                pages.push(totalPages);
            }
            else if (number > 5 && number <= totalPages - 3) {
                pages.push(1);
                pages.push('...');

                for (let n = number - 2; n <= number + 2; n++) {
                    pages.push(n);
                }
                pages.push('...');
                pages.push(totalPages);
            }
            else {
                pages.push(1);
                pages.push('...');

                for (let n = totalPages - 5; n <= totalPages; n++) {
                    pages.push(n);
                }
            }
            return pages;
        },
    },
    methods: {
        ...mapActions(useReviewStore, ['fetchReviews']),
        fetchPage(page, size = 10) {
            this.fetchReviews(this.productIdx, page, size);
        },
    },

    watch: {
        productIdx() {
            this.fetchPage(0, 10);
        },
    },

    beforeMount() {
        const route = useRoute();
        this.productIdx = route.params.idx;
        this.fetchPage(0);
    }
};
